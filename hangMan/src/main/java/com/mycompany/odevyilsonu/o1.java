    private void btnHarfTahminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHarfTahminActionPerformed
        bildiMi = false;
        String olasiHarf = tahminHarf.getText().toUpperCase();
        if (olasiHarf.isEmpty()) {
            return;
        }

        char olasiHarfler[] = olasiHarf.toCharArray();

        for (char c : olasiHarfler) {
            for (int i = 0; i < secilenKelime.length(); i++) {
                if (c == secilenKelime.charAt(i)) {
                    harfEtiketleri[i].setText(" " + c + " ");
                    bildiMi = true;
                }
            }
        }
        tahminHarf.setText("");
        if (!(bildiMi)) {
            resimGuncelle(tahminSayisi);
            tahminSayisi++;
        }

        if (kelimeTamamenAcildiMi()) {
            resimGuncelle(271);
            oyunKaydet("GALIBIYET", gecenSureSaniye);
            oyunTimer.stop();
            setOyunButonlariAktif(false);
        } else if (tahminSayisi > 11) {
            oyunKaydet("MAGLUBIYET", gecenSureSaniye);
            oyunTimer.stop();
            setOyunButonlariAktif(false);
        }


    }//GEN-LAST:event_btnHarfTahminActionPerformed

    private void btnKelimeTahminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKelimeTahminActionPerformed
        String tahminKelime = tahminKelimetxt.getText().toUpperCase().trim();
        if (tahminKelime.isEmpty()) {
            return;
        }

        if (tahminKelime.equals(secilenKelime)) {
            for (int i = 0; i < secilenKelime.length(); i++) {
                harfEtiketleri[i].setText(" " + secilenKelime.charAt(i) + " ");
            }
            resimGuncelle(271);
            oyunKaydet("GALIBIYET", gecenSureSaniye);
            oyunTimer.stop();
            setOyunButonlariAktif(false);
        } else {
            resimGuncelle(tahminSayisi);
            tahminSayisi++;
        }

        if (tahminSayisi > 11) {
            oyunKaydet("MAGLUBIYET", gecenSureSaniye);
            oyunTimer.stop();
            setOyunButonlariAktif(false);
        }
        tahminKelimetxt.setText("");
    }//GEN-LAST:event_btnKelimeTahminActionPerformed

