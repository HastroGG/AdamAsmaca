    public void oyunlariYukle(DefaultTableModel tableModel) {
        String logDosyasi = "C:\\P2Oyun\\TXTDosyalar\\oyunlar.txt";
        tableModel.setRowCount(0);
        
        try (BufferedReader br = new BufferedReader(new FileReader(logDosyasi))) {
            String satir;
            while ((satir = br.readLine()) != null) {
                try {

                    int durumBaslangic = satir.indexOf("Oyun Durumu: ") + "Oyun Durumu: ".length();
                    int durumBitis = satir.indexOf(" - Geçen Süre:");
                    String oyunDurumu = satir.substring(durumBaslangic, durumBitis).trim();

                    int sureBaslangic = satir.indexOf("Geçen Süre: ") + "Geçen Süre: ".length();
                    int sureBitis = satir.indexOf(" saniye");
                    String sureSaniye = satir.substring(sureBaslangic, sureBitis).trim();

                    tableModel.addRow(new Object[]{oyunDurumu, sureSaniye});

                } catch (Exception e) {
                    System.out.println("Satır ayrıştırılamadı, atlanıyor: " + satir);
                }
            }
        } catch (Exception e) {
            System.out.println("Dosya okunurken hata oluştu: " + e.getMessage());
        }
    }

