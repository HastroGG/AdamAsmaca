    private void logKaydet(String kullaniciGirdisi, String durum) {
        String logDosyasi = "C:\\P2Oyun\\TXTDosyalar\\log.txt";
        java.time.format.DateTimeFormatter dtf = java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String zaman = java.time.LocalDateTime.now().format(dtf);
        String logSatiri = String.format("[%s] Giriş Denemesi - Girilen: '%s' - Durum: %s%n", zaman, kullaniciGirdisi, durum);
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(logDosyasi, true))) {
            bw.write(logSatiri);
        } catch (IOException e) {
            System.out.println("Log yazılırken hata oluştu: " + e.getMessage());
        }
    }

