    private void logYukle(DefaultTableModel model2) {
        String logDosyasi = "C:\\P2Oyun\\TXTDosyalar\\log.txt";
        model2.setRowCount(0);

        try (BufferedReader br = new BufferedReader(new FileReader(logDosyasi))) {
            String satir;

            while ((satir = br.readLine()) != null) {
                try {
                    String zaman = satir.substring(satir.indexOf("[") + 1, satir.indexOf("]"));

                    int girilenBaslangic = satir.indexOf("Girilen: '") + "Girilen: '".length();
                    int girilenBitis = satir.indexOf("'", girilenBaslangic);
                    String girilenDeger = satir.substring(girilenBaslangic, girilenBitis);

                    int durumBaslangic = satir.indexOf("Durum: ") + "Durum: ".length();
                    String durum = satir.substring(durumBaslangic).trim();

                    model2.addRow(new Object[]{zaman, girilenDeger, durum});

                } catch (Exception e) {
                    System.out.println("Log satırı çözümlenemedi: " + satir);
                }
            }
        } catch (IOException e) {
            System.out.println("Log dosyası okunurken hata oluştu: " + e.getMessage());
        }

    }

