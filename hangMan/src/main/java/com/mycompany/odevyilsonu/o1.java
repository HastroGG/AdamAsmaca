    private boolean kelimeTamamenAcildiMi() {
        if (harfEtiketleri == null) {
            return false;
        }
        for (javax.swing.JLabel label : harfEtiketleri) {
            if (label.getText().contains("*")) {
                return false;
            }
        }
        resimGuncelle(271);
        return true;
    }

    private void resimGuncelle(int tahminSayisi) {
        try {
            String resimYolu = "C:\\P2Oyun\\Resimler\\" + tahminSayisi + ".jpg";
            java.io.File dosya = new java.io.File(resimYolu);

            if (dosya.exists()) {

                javax.swing.ImageIcon icon = new javax.swing.ImageIcon(resimYolu);
                java.awt.Image img = icon.getImage();
                java.awt.Image yeniImg = img.getScaledInstance(pnlDaragaci.getWidth(), pnlDaragaci.getHeight(), java.awt.Image.SCALE_SMOOTH);

                daragaciResimEtiketi.setIcon(new javax.swing.ImageIcon(yeniImg));
            } else {
                System.out.println("Resim bulunamadı: " + resimYolu);
                daragaciResimEtiketi.setIcon(null);
            }
            pnlDaragaci.revalidate();
            pnlDaragaci.repaint();
        } catch (Exception e) {
            System.out.println("Resim yükleme hatası: " + e.getMessage());
        }
    }
